BOOL gettingtheprocessids(DWORD* pids, SIZE_T cbneeded,LPSTR processname,HANDLE *hprocess) {
    CHAR basename[max_path] = { 0 };

    

    for (DWORD i = 0; i < cbneeded / sizeof(DWORD); i++) {
        if (pids[i] != 0) {
            printf("The process id is %d\n", pids[i]);
            HANDLE hprocesss = OpenProcess(PROCESS_QUERY_INFORMATION | PROCESS_VM_READ, FALSE, pids[i]);

            if (hprocesss == NULL) {
                printf("openprocess failed with error no %d\n", GetLastError());
                continue;
            }

            HMODULE hmodules;
            DWORD cbneededs;
            if (!EnumProcessModules(hprocesss, &hmodules, sizeof(hmodules), &cbneededs)) {
                printf("enumprocessmodules failed with error no %d\n", GetLastError());
                CloseHandle(hprocesss);
                continue;
            }



            GetModuleBaseNameA(hprocesss, hmodules, basename, sizeof(basename));
            
            //printf("the process name is %s\n", basename);
            if (strcmp(basename, processname) == 0 ) {
                printf("process name found %s\n", basename);
                *hprocess = hprocesss;
                break;
  
            }
            else {
                continue;
            }
          

            CloseHandle(hprocess);
        }
    }


    
    return TRUE;
}

    








